    private void seleccionar() {
        this.txtBuscar.setText("");
        int fila = this.tablaVivienda.getSelectedRow();
        if (fila != -1) {
            if (opcion == 1) {
                frmModfProp.objViv = MTV.getVivienda(fila);
                frmModfProp.txtVivienda.setText(MTV.getVivienda(fila).getDireccion() + " " + MTV.getVivienda(fila).getNroVivienda());
                frmModfProp.txtVivienda.requestFocus();
            }

            if (opcion == 2) {
                frmCaja.objV = MTV.getVivienda(fila);
                frmCaja.txtBuscar.setText(MTV.getVivienda(fila).getObjCont().toString());
                frmCaja.txtBuscar.requestFocus();
            }

            this.dispose();

            JInternalFrame[] frames = frmPrincipalAgua.PanelPrincipal.getAllFrames();
            for (JInternalFrame frame : frames) {
                if (frame.isIcon()) {
                    try {
                        frame.setIcon(false);
                        frame.setSelected(true);
                        frame.setVisible(true);
                        frmPrincipalAgua.PanelPrincipal.getDesktopManager().deiconifyFrame(frame);
                    } catch (PropertyVetoException e) {
                        e.printStackTrace();
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Usted no a seleccionado un campo en la tabla.", "MENSAJE --> VIVIENDA", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void salir() {
        this.dispose();
        JInternalFrame[] frames = frmPrincipalAgua.PanelPrincipal.getAllFrames();
        for (JInternalFrame frame : frames) {
            if (frame.isIcon()) {
                try {
                    frame.setIcon(false);
                    frame.setSelected(true);
                    frame.setVisible(true);
                    frmPrincipalAgua.PanelPrincipal.getDesktopManager().deiconifyFrame(frame);
                } catch (PropertyVetoException e) {
                    e.printStackTrace();
                }
            }
        }
    }

    private void buscar() {
        String buscar = this.txtBuscar.getText().trim();
        ArrayList<Vivienda> ltsVivienda = new ArrayList();
        try {
            if (this.btnPerNatu.isSelected()) {
                ltsVivienda = ViviendaBo.buscarDNIContribuyente(buscar);
                if (ltsVivienda.isEmpty()) {
                    ltsVivienda = ViviendaBo.buscarNombreContribuyente(buscar);
                }
                if (ltsVivienda.isEmpty()) {
                    ltsVivienda = ViviendaBo.buscarApellidoContribuyente(buscar);
                }
                if (ltsVivienda.isEmpty()) {
                    ltsVivienda = ViviendaBo.buscarDireccionContribuyente(buscar);
                }
            } else {
                ltsVivienda = ViviendaBo.buscarRazonSocialContribuyente(buscar);
                if (ltsVivienda.isEmpty()) {
                    ltsVivienda = ViviendaBo.buscarDNIContribuyenteRazonSocial(buscar);
                }
                if (ltsVivienda.isEmpty()) {
                    ltsVivienda = ViviendaBo.buscarDireccionRazonSocialContribuyente(buscar);
                }
            }
            for (int i = 0; i < ltsVivienda.size(); i++) {
                if (ltsVivienda.get(i).getEstado().equals("No")) {
                    ltsVivienda.remove(i);
                }
            }
            MTV.setListado_Vivienda(ltsVivienda);
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
