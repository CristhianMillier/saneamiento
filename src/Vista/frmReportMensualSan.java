    private void cargarAnio() {
        Calendar calendar = Calendar.getInstance();
        int year = calendar.get(Calendar.YEAR);

        DefaultComboBoxModel<Integer> yearModel = new DefaultComboBoxModel<>();
        for (int i = year; i >= 2015; i--) {
            yearModel.addElement(i);
        }

        this.comboAnio.setModel(yearModel);
        this.comboAnio.setSelectedItem(year);
    }

    private void cargarMes() {
        Calendar calendar = Calendar.getInstance();
        int month = calendar.get(Calendar.MONTH);

        DefaultComboBoxModel<String> monthModel = new DefaultComboBoxModel<>();
        String[] months = new String[]{"ENERO", "FEBRERO", "MARZO", "ABRIL", "MAYO", "JUNIO", "JULIO",
            "AGOSTO", "SETIEMBRE", "OCTUBRE", "NOVIEMBRE", "DICIEMBRE"};
        for (int i = 0; i < months.length; i++) {
            monthModel.addElement(months[i]);
        }

        this.comboMes.setModel(monthModel);
        this.comboMes.setSelectedItem(month);
    }

    private void botones() {
        this.btnGenerarReporte.setOpaque(false);
        this.btnGenerarReporte.setContentAreaFilled(false);
        this.btnGenerarReporte.setBorderPainted(false);
        this.btnGenerarReporte.setToolTipText("Generar Reporte");
    }

    private void generarReporte() {
        int anio = (Integer) this.comboAnio.getSelectedItem();
        int mes = this.comboMes.getSelectedIndex() + 1;
        try {
            MensualSanReport reporte = new MensualSanReport();
            reporte.verReporte(CajaBo.reportMensualSan(anio, mes), mes, anio);
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
