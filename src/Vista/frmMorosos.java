    private void cargarTabla() {
        ArrayList<Morosos> ltsMorosos = new ArrayList();
        try {
            if (this.btnPerNatu.isSelected()) {
                ltsMorosos = MorososBo.obtenerListaMoroso();
            } else {
                ltsMorosos = MorososBo.obtenerListaContribuyenteRazonSocial();
            }
            MTM.setListado_Morosos(ltsMorosos);
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

