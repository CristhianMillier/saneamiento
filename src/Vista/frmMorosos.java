    private void buscar() {
        String buscar = this.txtBuscar.getText().trim();
        ArrayList<Morosos> ltsMorosos = new ArrayList();
        try {
            if (this.btnPerNatu.isSelected()) {
                ltsMorosos = MorososBo.buscarDNIContribuyente(buscar);
                if (ltsMorosos.isEmpty()) {
                    ltsMorosos = MorososBo.buscarNombreContribuyente(buscar);
                }
                if (ltsMorosos.isEmpty()) {
                    ltsMorosos = MorososBo.buscarApellidoContribuyente(buscar);
                }
                if (ltsMorosos.isEmpty()) {
                    ltsMorosos = MorososBo.buscarDireccionContribuyente(buscar);
                }
            } else {
                ltsMorosos = MorososBo.buscarRazonSocialContribuyente(buscar);
                if (ltsMorosos.isEmpty()) {
                    ltsMorosos = MorososBo.buscarDNIContribuyenteRazonSocial(buscar);
                }
                if (ltsMorosos.isEmpty()) {
                    ltsMorosos = MorososBo.buscarDireccionRazonSocialContribuyente(buscar);
                }
            }
            MTM.setListado_Morosos(ltsMorosos);
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

    private void botonos() {
        this.btnCancelar.setOpaque(false);
        this.btnCancelar.setContentAreaFilled(false);
        this.btnCancelar.setBorderPainted(false);
        this.btnCancelar.setToolTipText("Cancelar");

        this.btnSalir.setOpaque(false);
        this.btnSalir.setContentAreaFilled(false);
        this.btnSalir.setBorderPainted(false);
        this.btnSalir.setToolTipText("Salir");

        this.btnVerMoroso.setOpaque(false);
        this.btnVerMoroso.setContentAreaFilled(false);
        this.btnVerMoroso.setBorderPainted(false);
        this.btnVerMoroso.setToolTipText("Ver Detalle Moroso");

        this.btnImprimir.setOpaque(false);
        this.btnImprimir.setContentAreaFilled(false);
        this.btnImprimir.setBorderPainted(false);
        this.btnImprimir.setToolTipText("Imprimir Moroso");
    }

    private void activarControles(boolean estado) {
        this.txtContribuyente.setEnabled(estado);
        this.txtDomicilio.setEnabled(estado);
        this.txtDeudaAct.setEnabled(estado);
        this.txtDeudaAnt.setEnabled(estado);
        this.txtDeudaTotal.setEnabled(estado);
        this.txtMeses.setEnabled(estado);
        this.btnCancelar.setEnabled(estado);
        this.btnImprimir.setEnabled(estado);

        this.btnVerMoroso.setEnabled(!estado);
        this.txtBuscar.setEnabled(!estado);
        this.tablaMorosos.setEnabled(!estado);
        this.btnRazSocial.setEnabled(!estado);
        this.btnPerNatu.setEnabled(!estado);

        if (estado) {
            tablaMorosos.setBackground(new java.awt.Color(255, 102, 102));
        } else {
            tablaMorosos.setBackground(new java.awt.Color(255, 255, 255));
        }
    }

    private void limpiar() {
        this.txtContribuyente.setText("");
        this.txtDomicilio.setText("");
        this.txtDeudaAct.setText("");
        this.txtDeudaAnt.setText("");
        this.txtDeudaTotal.setText("");
        this.txtMeses.setText("");

        this.objM = null;
    }

    private void verMoroso() {
        this.txtBuscar.setText("");
        int fila = this.tablaMorosos.getSelectedRow();
        if (fila != -1) {
            this.objM = MTM.getMorosos(fila);
            this.activarControles(true);
            this.cargarDatos(objM);
            this.cargarTabla();
        } else {
            JOptionPane.showMessageDialog(this, "Usted no a seleccionado un campo en la tabla.", "MENSAJE --> CONTRIBUYENTE", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void cargarDatos(Morosos objM) {
        this.txtContribuyente.setText(objM.getObjVivienda().getObjCont().toString());
        this.txtDomicilio.setText(objM.getObjVivienda().getDireccion() + " " + objM.getObjVivienda().getNroVivienda());
        this.txtDeudaAct.setText(String.valueOf(objM.getDeudaAct()));
        this.txtDeudaAnt.setText(String.valueOf(objM.getDeudaAnt()));
        this.txtDeudaTotal.setText(String.valueOf(objM.getDeudaTotal()));
        this.txtMeses.setText(objM.getMeses());
    }

    private void imprimir() {
        try {
            MorososReport report = new MorososReport();
            report.verReporte(this.objM.getObjVivienda().getIdVivienda(), this.objM.getDeudaTotal(), this.objM.getObjVivienda().getObjCont().toString());
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        this.limpiar();
        this.activarControles(false);
        this.cargarTabla();
    }
